double
uniformDistribution(double lowerBound, double upperBound)
{

	double * scaledUniformSamples = calloc(1000, sizeof(double));

	if (scaledUniformSamples == NULL)
	{
		fprintf(stderr, "Memory allocation failed!\n");
		abort();
	}

	if (lowerBound >= upperBound)
	{
		fprintf(stderr, "The lower bound must be strictly greater than the upper bound.\n");
		abort();
	}

	for (size_t i = 0; i < numberOfSamples; i++)
	{
		scaledUniformSamples[i] = lowerBound + uniformSamples[i] * (upperBound - lowerBound);
	}

	double dist = libUncertainDoubleDistFromSamples(scaledUniformSamples, numberOfSamples);

	free(scaledUniformSamples);

	return dist;
}
